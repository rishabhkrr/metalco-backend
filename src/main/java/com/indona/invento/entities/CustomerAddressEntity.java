package com.indona.invento.entities;

import com.fasterxml.jackson.annotation.JsonIgnore;

import jakarta.persistence.*;
import lombok.*;

@Entity
@Table(name = "customer_address")
@Data
@NoArgsConstructor
@AllArgsConstructor
@Builder
public class CustomerAddressEntity {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @Column(name = "is_primary")
    private Boolean Primary;           // TRUE if it's the primary address
    private String branchName;             // First Letter Upper for all words
    private String address;                // Full Address Line
    private String supplierArea;           // First Letter Upper
    private String state;                  // Autogenerated from pincode or manual
    private String country;                // Autogenerated from pincode or manual
    private String pincode;                // Number
    private String mapLocation;           // Link to Map / geo-coordinates

    @ManyToOne(fetch = FetchType.LAZY)
    @JoinColumn(name = "customer_id")
    @JsonIgnore
    private CustomerMasterEntity customer;
}
